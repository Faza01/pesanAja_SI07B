    private void tblProdukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdukMouseClicked
        int row = tblProduk.getSelectedRow();

        if (row != -1) {
            // Ambil data dari row yang dipilih
            String idProduk = tblProduk.getValueAt(row, 0).toString(); // ID Produk
            String nama = tblProduk.getValueAt(row, 1).toString(); // Nama Produk
            String deskripsi = tblProduk.getValueAt(row, 2).toString(); // Deskripsi
            String harga = tblProduk.getValueAt(row, 3).toString(); // Harga
            String tersedia = tblProduk.getValueAt(row, 4).toString(); // Status Tersedia

            // Mengisi nilai ke form
            lblId_produk.setText(idProduk);
            txtNama.setText(nama);
            txtDeskripsi.setText(deskripsi);
            txtHarga.setText(harga);

            // Jika tipe produk adalah Makanan atau Minuman, tentukan kategori atau penyajian
            String tipeProduk = tblProduk.getValueAt(row, 5).toString();
            cbxTipe.setSelectedItem(tipeProduk);
            if (tipeProduk.equalsIgnoreCase("makanan")) {
                // Misalkan kategori disimpan pada kolom lain jika ada
                String kategori = tblProduk.getValueAt(row, 6).toString(); // Ambil kategori
                txtKategori.setSelectedItem(kategori);
            } else {
                // Misalkan penyajian disimpan pada kolom lain jika ada
                String penyajian = tblProduk.getValueAt(row, 7).toString(); // Ambil penyajian
                txtPenyajian.setSelectedItem(penyajian);
            }

            // Jika tersedia "Tersedia", set checkbox ke true
            cbxKetersediaan.setSelectedItem(tersedia.equalsIgnoreCase("tersedia"));

            // Untuk gambar, misalnya ditampilkan di txtGambar
            String gambar = tblProduk.getValueAt(row, 8).toString(); // Ambil gambar
            txtGambar.setText(gambar);

        }
    }//GEN-LAST:event_tblProdukMouseClicked


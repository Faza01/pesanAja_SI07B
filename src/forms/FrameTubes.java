    private void btnSimpanPerubahanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanPerubahanActionPerformed
        // Mengambil input dari form
        int idProduk = Integer.parseInt(lblId_produk.getText());
        String nama = txtNama.getText();
        String deskripsi = txtDeskripsi.getText();
        String hargaText = txtHarga.getText();
        String kategori = txtKategori.getSelectedItem().toString();
        String penyajian = txtPenyajian.getSelectedItem().toString();
        String gambar = txtGambar.getText();
        String tipe = cbxTipe.getSelectedItem().toString(); // Pilihan: "Makanan" atau "Minuman"
        boolean tersedia = cbxKetersediaan.getSelectedItem().toString().equalsIgnoreCase("Tersedia");

        // Validasi input
        if (hargaText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kolom harga perlu diisi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            double harga = Double.parseDouble(hargaText); // Konversi harga ke double

            ProdukDB produkDB = new ProdukDB();

            if (tipe.equalsIgnoreCase("makanan")) {
                // Buat objek Makanan
                Makanan makanan = new Makanan(nama, harga, deskripsi, tersedia, kategori);
                makanan.setGambar(gambar);

                // Tambahkan ke database
                produkDB.updateProduk(makanan, idProduk);

            } else {
                // Buat objek Minuman
                Minuman minuman = new Minuman(nama, harga, deskripsi, tersedia, penyajian);
                minuman.setGambar(gambar);

                // Tambahkan ke database
                produkDB.updateProduk(minuman, idProduk);
            }

            // Berikan feedback kepada pengguna
            JOptionPane.showMessageDialog(this, "Produk berhasil diUpdate!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // Refresh tampilan produk (misalnya, reload data di tabel atau panel)
            loadProduk();

            // Kosongkan form
            txtNama.setText("");
            txtDeskripsi.setText("");
            txtHarga.setText("");
            txtGambar.setText("");
            txtKategori.setSelectedIndex(0);
            txtPenyajian.setSelectedIndex(0);
            cbxTipe.setSelectedIndex(0);
            cbxKetersediaan.setSelectedIndex(0);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanPerubahanActionPerformed


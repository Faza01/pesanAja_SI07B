    private void btnTambahProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahProdukActionPerformed
        // Mengambil input dari form
        String nama = txtNama.getText();
        String deskripsi = txtDeskripsi.getText();
        String hargaText = txtHarga.getText();
        String kategori = txtKategori.getSelectedItem().toString();
        String penyajian = txtPenyajian.getSelectedItem().toString();
        String gambar = txtGambar.getText();
        String tipe = cbxTipe.getSelectedItem().toString(); // Pilihan: "Makanan" atau "Minuman"
        boolean tersedia = cbxKetersediaan.getSelectedItem().toString().equalsIgnoreCase("Tersedia");

        // Validasi input
        if (hargaText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Kolom harga perlu diisi", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            double harga = Double.parseDouble(hargaText); // Konversi harga ke double

            ProdukDB produkDB = new ProdukDB();

            if (tipe.equalsIgnoreCase("makanan")) {
                // Buat objek Makanan
                Makanan makanan = new Makanan(nama, harga, deskripsi, tersedia, kategori);
                makanan.setGambar(gambar);

                // Tambahkan ke database
                produkDB.tambahProduk(makanan);

            } else {
                // Buat objek Minuman
                Minuman minuman = new Minuman(nama, harga, deskripsi, tersedia, penyajian);
                minuman.setGambar(gambar);

                // Tambahkan ke database
                produkDB.tambahProduk(minuman);
            }

            // Berikan feedback kepada pengguna
            JOptionPane.showMessageDialog(this, "Produk berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // Refresh tampilan produk (misalnya, reload data di tabel atau panel)
            loadProduk();

            // Kosongkan form
            txtNama.setText("");
            txtDeskripsi.setText("");
            txtHarga.setText("");
            txtGambar.setText("");
            txtKategori.setSelectedIndex(0);
            txtPenyajian.setSelectedIndex(0);
            cbxTipe.setSelectedIndex(0);
            cbxKetersediaan.setSelectedIndex(0);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnTambahProdukActionPerformed

    private void loadProduk() {
        // Membuat koneksi ke database
        ProdukDB produkDB = new ProdukDB();

        // Mengambil semua produk dari database
        List<Produk> produkList = produkDB.semuaProduk();

        // Menyiapkan data untuk mengisi JTable
        DefaultTableModel model = (DefaultTableModel) tblProduk.getModel();
        model.setRowCount(0); // Menghapus data lama di tabel

        List<Makanan> makananList = new ArrayList<>();
        List<Minuman> minumanList = new ArrayList<>();
        for (Produk produk : produkList) {
            if (produk instanceof Makanan) {
                makananList.add((Makanan) produk);
            } else if (produk instanceof Minuman) {
                minumanList.add((Minuman) produk);
            }
        }
        // Gunakan makananList dan minumanList sesuai kebutuhan
        for (Makanan makanan : makananList) {
            Object[] row = new Object[9]; // Sesuaikan dengan jumlah kolom di tabel
            row[0] = makanan.getIdProduk();
            row[1] = makanan.getNamaMakanan(); // Asumsikan produk memiliki nama
            row[2] = makanan.getDeskripsi();
            row[3] = makanan.getHarga();
            row[4] = makanan.isTersedia() ? "Tersedia" : "Tidak Tersedia";
            row[5] = "makanan";
            row[6] = makanan.getKategoriMakanan();
            row[7] = "";
            row[8] = makanan.getGambar();

            // Menambahkan baris ke tabel
            model.addRow(row);
        }
        for (Minuman minuman : minumanList) {
            Object[] row = new Object[9]; // Sesuaikan dengan jumlah kolom di tabel
            row[0] = minuman.getIdProduk();
            row[1] = minuman.getNamaMinuman(); // Asumsikan produk memiliki nama
            row[2] = minuman.getDeskripsi();
            row[3] = minuman.getHarga();
            row[4] = minuman.isTersedia() ? "Tersedia" : "Tidak Tersedia";
            row[5] = "minuman";
            row[6] = "";
            row[7] = minuman.getPenyajian();
            row[8] = minuman.getGambar();

            // Menambahkan baris ke tabel
            model.addRow(row);
        }
    }

